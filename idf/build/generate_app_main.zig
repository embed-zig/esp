const std = @import("std");

pub fn main() !void {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const allocator = gpa.allocator();

    const args = try std.process.argsAlloc(allocator);
    defer std.process.argsFree(allocator, args);

    if (args.len != 3) {
        std.debug.print("usage: {s} <output_file> <delegate_symbol>\n", .{args[0]});
        return error.InvalidArguments;
    }

    const output_file = args[1];
    const delegate_symbol = args[2];

    try validateCSymbol(delegate_symbol);

    const output_dir = std.fs.path.dirname(output_file) orelse ".";
    try std.fs.cwd().makePath(output_dir);

    const content = try std.fmt.allocPrint(
        allocator,
        "// Auto-generated by idf/build/generate_app_main.zig.\n" ++
            "extern void {s}(void);\n\n" ++
            "void app_main(void)\n" ++
            "{{\n" ++
            "    {s}();\n" ++
            "}}\n",
        .{ delegate_symbol, delegate_symbol },
    );
    defer allocator.free(content);

    try std.fs.cwd().writeFile(.{
        .sub_path = output_file,
        .data = content,
    });
}

fn validateCSymbol(symbol: []const u8) !void {
    if (symbol.len == 0) {
        return error.InvalidCSymbol;
    }

    if (!(std.ascii.isAlphabetic(symbol[0]) or symbol[0] == '_')) {
        return error.InvalidCSymbol;
    }

    for (symbol[1..]) |ch| {
        if (!(std.ascii.isAlphanumeric(ch) or ch == '_')) {
            return error.InvalidCSymbol;
        }
    }
}

test "validateCSymbol accepts valid function name" {
    try validateCSymbol("espz_wifi_scan_example_main");
}

test "validateCSymbol rejects invalid function name" {
    try std.testing.expectError(error.InvalidCSymbol, validateCSymbol("1bad"));
    try std.testing.expectError(error.InvalidCSymbol, validateCSymbol("bad-name"));
}
